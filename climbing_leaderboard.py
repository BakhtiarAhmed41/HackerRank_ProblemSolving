def climbingLeaderboard(ranked, player):
    new_ranked_unique = sorted(set(ranked), reverse=True)
    result = []
    index = len(new_ranked_unique)
    
    for score in player:
        while index > 0 and score >= new_ranked_unique[index - 1]:
            index -= 1
        result.append(index + 1)
    
    return result


